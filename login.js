function forgetEmailOrPasswordOpenForm(showhide){
    if(showhide == "show"){
    document.getElementById("loginHelpForm").style.visibility="visible";
    }else if(showhide == "hide"){
    document.getElementById("loginHelpForm").style.visibility="hidden";
    }
}

